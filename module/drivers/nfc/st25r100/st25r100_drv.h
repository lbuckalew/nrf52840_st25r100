#ifndef ST25R100_DRV_H
#define ST25R100_DRV_H

#define ST25_REG_OP 0x00
#define ST25_OP_WU_EN_FIELD 1
#define ST25_OP_EN_FIELD 1 << 1
#define ST25_OP_AM_EN_FIELD 1 << 3
#define ST25_OP_RX_EN_FIELD 1 << 4
#define ST25_OP_TX_EN_FIELD 1 << 5

#define ST25_REG_GENERAL 0x01
#define ST25_GENERAL_REG_S 1
#define ST25_GENERAL_MISO_PD1 1 << 2
#define ST25_GENERAL_MISO_PD2 1 << 3

#define ST25_REG_REGULATOR_CFG 0x02
#define ST25_REGULATOR_CFG_REGD_FIELD 7 << 5

#define ST25_REG_TX_DRIVER 0x03
#define ST25_TX_DRIVER_DRES 0x0F

#define ST25_REG_TX_MOD1 0x04
#define ST25_TX_MOD1_REG_AM 1
#define ST25_TX_MOD1_AM_INDEX 0xF << 4

#define ST25_REG_ANALOG_2 0x07
#define ST25_ANALOG_2_AFE_TD 0x0F

#define ST25_REG_RX_DIGITAL 0x08
#define ST25_RX_DIGITAL_LPF 7 << 4
#define ST25_RX_DIGITAL_HPF 3 << 2
#define ST25_RX_DIGITAL_AGC 1 << 7

#define ST25_REG_CORREL_1 0x09
#define ST25_CORREL_1_IIR1 0x0F
#define ST25_CORREL_1_IIR2 0x0F << 4

#define ST25_REG_CORREL_4 0x0C
#define ST25_CORREL_4_COLL_LVL 0x0F << 4

#define ST25_REG_CORREL_5 0x0D
#define ST25_CORREL_5_SUBC_EN 1 << 4
#define ST25_CORREL_5_IIR_F 0x07

#define ST25_REG_DR1 0x0F
#define ST25_DR1_OSC_OK_FIELD 1 << 5
#define ST25_DR1_AGD_OK_FIELD 1 << 6

#define ST25_REG_PROTOCOL 0x12
#define ST25_PROTOCOL_MODE_FIELD 0xF
#define ST25_PROTOCOL_MODE_NFCV 5

#define ST25_REG_TX_PROTOCOL 0x13
#define ST25_TX_PROTOCOL_TR_AM 1 << 4
#define ST25_TX_PROTOCOL_CRC 1 << 5
#define ST25_TX_PROTOCOL_PAR 1 << 6

#define ST25_REG_RX_PROTOCOL 0x16
#define ST25_RX_PROTOCOL_ANTCTL 1
#define ST25_RX_PROTOCOL_CRC 1 << 2
#define ST25_RX_PROTOCOL_PAR 1 << 3

#define ST25_REG_MASK_RX_TIMER 0x1C

#define ST25_REG_FIFO_FRAME1 0x34
#define ST25_FIFO_FRAME1_NTXMSB_FIELD 0xFF

#define ST25_REG_FIFO_FRAME2 0x35
#define ST25_FIFO_FRAME2_NTXLSB_FIELD 0x1F << 3

#define ST25_REG_FIFO_SR1 0x36

#define ST25_REG_FIFO_SR2 0x37

#define ST25_REG_IRQ_SR1 0x3C

#define ST25_REG_IRQ_SR2 0x3D

#define ST25_REG_IRQ_SR3 0x3E

#define ST25_REG_ID 0x3F
#define ST25_VAL_ID 0xA9

#define ST25_REG_FIFO 0x5F

#define ST25_CMD_SET_DEFAULT 0x60
#define ST25_CMD_STOP_ACT 0x62
#define ST25_CMD_CLR_FIFO 0x64
#define ST25_CMD_CLR_RX_GAIN 0X66
#define ST25_CMD_ADJ_REG 0x68
#define ST25_CMD_TX_DATA 0x6A
#define ST25_CMD_TX_EOF 0x6C
#define ST25_CMD_UNMASK_RX 0x72

#endif